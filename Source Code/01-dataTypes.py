# This is my 2nd app with Python to apply for data types

# All data in Python is an object

print(type(10))  # => Integer
print(type(-5.25)) # => Float
print(type("Hello")) # => String
print(type([1,2,3,4,5])) # => List
print(type((1,2,3,4,5))) # => Tuple
print(type({"One" : 1, "Two" : 2, "Three" : 3})) # => Dictionary
print(type(1 == 1)) # => Boolean

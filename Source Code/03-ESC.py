# This file is about Escape Sequences Characters

# '\b' => Back space
print("Hello\b World!") # Will remove the letter 'o'

# '\' => Escapes new line
print("My \
Name \
Is \
Kareem")

# '\\' => Escape the back slash character
print("My Name Is\\ Kareem")

# '\'' => Escape single quote
print('\'I Love Python\'')

# '\"' => Escape double quote
print("\"I Love Python\"")

# '\n' => Create a new line
print("My Name Is:\nKareem")

# '\r' => Carriage return
print("12345\rabcd")

# '\t' => Horizontal tap
print("Hello\tPython")

# '\xhh' = > Character hex value
print("\x4B\x54") # In Hex 4B = K & 54 = T
